#include "fillit.h"

void	clear_cord(char **map, t_tetris *t)
{
	int	n;

	n = 0;
	while (n < 4)
	{
		map[t->cord[0] + t->points[n].x][t->cord[1] + t->points[n].y] = '.';
		n++;
	}
}
