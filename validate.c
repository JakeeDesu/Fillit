#include "fillit.h"

int	v_line(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != '\n' && str[i] != '\0')
	{
		if (!(str[i] == '.' || str[i] == '#'))
			j++;
		i++;
	}
	if (i == 4 && j == 0)
		return (1);
	if (i == 0)
		return (2);
	return (0);
}
