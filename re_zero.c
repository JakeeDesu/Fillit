#include "fillit.h"

void	re_zero(t_tetris **t)
{
	int	i;

	i = 0;
	while (i < count_tetris(t))
	{
		t[i]->cord[0] = 0;
		t[i]->cord[1] = 0;
		t[i]->x = 0;
		i++;
	}
}
